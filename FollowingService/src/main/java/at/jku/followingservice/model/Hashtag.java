import java.util.HashSet;
import java.util.Set;

@Node
public class Hashtag {
    @Id
    String title;

    private Hashtag() {
    }

    public Hashtag(String title) {
        this.title = title;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
